<template>
  <div>
    <!-- 总数卡片 -->
    <el-row :gutter="20"
            class="mgb20">
      <el-col :span="6">
        <el-card>
          <div class="grid-content">
            <div class="grid-cont-center">
              <div class="grid-num">
                {{consumerCount}}
              </div>
              <div>用户总数</div>
            </div>
          </div>
        </el-card>
      </el-col>
      <el-col :span="6">
        <el-card>
          <div class="grid-content">
            <div class="grid-cont-center">
              <div class="grid-num">
                {{singercount}}
              </div>
              <div>歌手总数</div>
            </div>
          </div>
        </el-card>
      </el-col>
      <el-col :span="6">
        <el-card>
          <div class="grid-content">
            <div class="grid-cont-center">
              <div class="grid-num">
                {{songcount}}
              </div>
              <div>歌曲总数</div>
            </div>
          </div>
        </el-card>
      </el-col>
      <el-col :span="6">
        <el-card>
          <div class="grid-content">
            <div class="grid-cont-center">
              <div class="grid-num">
                {{songlistcount}}
              </div>
              <div>歌单总数</div>
            </div>
          </div>
        </el-card>
      </el-col>

    </el-row>

    <!-- 用户歌单风格 -->
    <el-row :gutter="20"
            class="mgb20">
      <!-- 用户性别比例 -->
      <el-col :span="12">
        <h3 class="mgb20">用户性别比例</h3>
        <div style="background-color:white">
          <ve-pie :data="consumerSex"
                  :theme='options'></ve-pie>
        </div>
      </el-col>
      <!-- 歌曲类型分布 -->
      <el-col :span="12">
        <h3 class="mgb20">歌曲类型分布</h3>
        <div style="background-color:white">
          <ve-histogram :data="songStyle"></ve-histogram>
        </div>
      </el-col>
    </el-row>

    <!-- 歌手 -->
    <el-row :gutter="20"
            class="mgb20">
      <!-- 性别比例 -->
      <el-col :span="12">
        <h3 class="mgb20">歌手性别比例</h3>
        <div style="background-color:white">
          <ve-pie :data='singerSex'></ve-pie>
        </div>
      </el-col>
      <!-- 国籍分布 -->
      <el-col :span="12">
        <h3 class="mgb20">歌手国籍分布</h3>
        <div style="background-color:white">
          <ve-histogram :data='country'></ve-histogram>
        </div>

      </el-col>
    </el-row>
  </div>
</template>

<script>
import { getAllConsumer, getAllSinger, allSong, getAllSongList } from '../api/index'
export default {
  data () {
    return {
      consumerCount: 0,//用户总数
      consumer: 0,//所有用户
      singercount: 0,//歌手总数
      songcount: 0,//歌曲总数
      songlistcount: 0,//歌单总数
      //用户性别比例
      consumerSex: {
        columns: ['性别', '总数'],
        rows: [
          { '性别': '男', '总数': 0 },
          { '性别': '女', '总数': 0 },
        ]
      },
      singerSex: {
        columns: ['性别', '总数'],
        rows: [
          { '性别': '男', '总数': 0 },
          { '性别': '女', '总数': 0 },
          { '性别': '组合', '总数': 0 },
          { '性别': '不明', '总数': 0 }
        ]
      },
      // 蓝色和粉红色
      options: {
        color: ['#87cefa', '#ffc0cb']
      },
      songStyle: {
        columns: ['风格', '总数'],
        rows: [
          { '风格': '华语', '总数': 0 },
          { '风格': '粤语', '总数': 0 },
          { '风格': '欧美', '总数': 0 },
          { '风格': '日韩', '总数': 0 },
          { '风格': 'BGM', '总数': 0 },
          { '风格': '轻音乐', '总数': 0 },
          { '风格': '乐器', '总数': 0 },
        ]
      },
      country: {
        columns: ['国籍', '总数'],
        rows: [
          { '国籍': '中国', '总数': 0 },
          { '国籍': '韩国', '总数': 0 },
          { '国籍': '日本', '总数': 0 },
          { '国籍': '美国', '总数': 0 },
          { '国籍': '新加坡', '总数': 0 },
          { '国籍': '意大利', '总数': 0 },
          { '国籍': '马来西亚', '总数': 0 },
          { '国籍': '西班牙', '总数': 0 },
        ]
      }
    }
  },
  mounted () {
    this.getConsumer()
    this.getSinger()
    this.getSong()
    this.getSonglist()
  },
  methods: {
    //根据性别获取用户数
    setSex (sex, val) {
      let count = 0
      for (let item of val) {
        if (sex == item.sex) {
          count++;
        }
      }
      return count;
    },
    // 用户总数
    getConsumer () {
      getAllConsumer().then(res => {
        this.consumer = res
        this.consumerCount = res.length
        this.consumerSex.rows[0]['总数'] = this.setSex(1, this.consumer)
        this.consumerSex.rows[1]['总数'] = this.setSex(0, this.consumer)
      })
    },
    // 获得歌手国籍
    getByCountry (location) {
      for (let item of this.country.rows) {
        if (location.includes(item['国籍'])) {
          item['总数']++;
        }
      }
    },
    // 歌手总数
    getSinger () {
      getAllSinger()
        .then(res => {
          // console.log('res');
          // console.log(res);
          this.singercount = res.length
          this.singerSex.rows[0]['总数'] = this.setSex(1, res)
          this.singerSex.rows[1]['总数'] = this.setSex(0, res)
          this.singerSex.rows[2]['总数'] = this.setSex(2, res)
          this.singerSex.rows[3]['总数'] = this.setSex(3, res)
          for (let item of res) {
            this.getByCountry(item.location);
          }
        })
    },
    // 歌曲总数
    getSong () {
      allSong()
        .then(res => {
          // console.log(res);
          this.songcount = res.length
        })
    },
    // 根据歌单风格获取总数
    getByStyle (style) {
      for (let item of this.songStyle.rows) {
        if (style.includes(item['风格'])) {
          item['总数']++
        }
      }
    },
    // 歌单总数
    getSonglist () {
      getAllSongList()
        .then(res => {
          this.songlistcount = res.length
          for (let item of res) {
            this.getByStyle(item.style)
          }
        })
    }
  }
}
</script>

